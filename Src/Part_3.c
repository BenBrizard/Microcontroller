void LCD_DrawPicture(const uint8_t* picture_chosen, uint16_t largeur, uint16_t hauteur, int16_t l_offset, int16_t h_offset, uint8_t resizing){
	static uint8_t iterateur_ligne = 0;
	static uint8_t iterateur_colonne = 0;
	for (uint16_t i=0; i<(largeur*hauteur); i++){
			iterateur_colonne = resizing*(i%largeur);
			for(uint8_t j=0; j<resizing; j++){
				if(picture_chosen[i]){ // On fait des cases de 2 pixels de large
					for(uint8_t m=0; m<resizing; m++){
					LCD_DrawPixel(iterateur_colonne+l_offset+m,iterateur_ligne+h_offset+j, WHITE);
					}
				}
				
			}
			if(i%largeur==0 && i>0){
					iterateur_ligne+=resizing;
				}
		}
}

